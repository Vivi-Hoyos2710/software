        int puntos = data.getUsuarios().get(correoUsuario).getPuntajeTotal();
        puntos += data.getUsuarios().get(correoUsuario).getHorarioUsuario().temporalidad[mes][dia][hora].getPuntuacion();
        data.getUsuarios().get(correoUsuario).setPuntajeTotal(puntos);
        data.getUsuarios().get(correoUsuario).getHorarioUsuario().temporalidad[mes][dia][hora] = null;
        this.dispose();
